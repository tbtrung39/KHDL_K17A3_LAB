def tinh_toan():
    a=int(input('nhập số a ='))
    b=int(input('nhập số b ='))
    cong= a+b
    tru = a- b
    nhan=a*b
    chia= a/b
    print('kết quả phép cộng', cong)
    print('kết quả phép trừ' ,tru)
    print('kết quả phép nhân', nhan)
    print('kết quả phép chia', chia)
    return
tinh_toan()