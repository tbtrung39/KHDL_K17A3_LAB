n = input("Nhập một số: ")
for i in n:
    if i == '0':
        print('không', end=' ')
    elif i == '1':
        print('một', end=' ')
    elif i == '2':
        print('hai', end=' ')
    elif i == '3':
        print('ba', end=' ')
    elif i == '4':
        print('bốn', end=' ')
    elif i == '5':
        print('năm', end=' ')
    elif i == '6':
        print('sáu', end=' ')
    elif i == '7':
        print('bảy', end=' ')
    elif i == '8':
        print('tám', end=' ')
    elif i == '9':
        print('chín', end=' ')
    else:
        print("Không phải là một số nguyên dương.")
        break
else:
    print()  