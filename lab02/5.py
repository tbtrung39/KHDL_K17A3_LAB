month = int(input("Nhập vào tháng trong năm (1-12): "))

if month == 1:
    print("Tháng này là January")
elif month == 2:
    print("Tháng này là February")
elif month == 3:
    print("Tháng này là March")
elif month == 4:
    print("Tháng này là April")
elif month == 5:
    print("Tháng này là May")
elif month == 6:
    print("Tháng này là June")
elif month == 7:
    print("Tháng này là July")
elif month == 8:
    print("Tháng này là August")
elif month == 9:
    print("Tháng này là September")
elif month == 10:
    print("Tháng này là October")
elif month == 11:
    print("Tháng này là November")
elif month == 12:
    print("Tháng này là December")
else:
    print("Số nhập vào không hợp lệ, vui lòng nhập lại từ 1 đến 12.")