month_number = int(input("Nhập vào một số từ 1 đến 12: "))
if month_number == 1:
    print(month_number,": January")
elif month_number == 2:
    print(month_number,": February")
elif month_number == 3:
    print(month_number,": March")
elif month_number == 4:
    print(month_number,": April")
elif month_number == 5:
    print(month_number,": May")
elif month_number == 6:
    print(month_number,": June")
elif month_number == 7:
    print(month_number,": July")
elif month_number == 8:
    print(month_number,": August")
elif month_number == 9:
    print(month_number,": September")
elif month_number == 10:
    print(month_number,": October")
elif month_number == 11:
    print(month_number,": November")
elif month_number == 12:
    print(month_number,": December")
else:
    print("Số bạn nhập không hợp lệ. Vui lòng nhập một số từ 1 đến 12.")
