day_number = int(input("Nhập vào một số từ 1 đến 7: "))
if day_number == 1:
        print("Thứ bạn đã nhập là: Sunday")
elif day_number == 2:
        print("Thứ bạn đã nhập là: Monday")
elif day_number == 3:
        print("Thứ bạn đã nhập là: Tuesday")
elif day_number == 4:
        print("Thứ bạn đã nhập là: Wednesday")
elif day_number == 5:
        print("Thứ bạn đã nhập là: Thursday")
elif day_number == 6:
        print("Thứ bạn đã nhập là: Friday")
elif day_number == 7:
        print("Thứ bạn đã nhập là: Saturday")
else:
        print("Số bạn nhập không hợp lệ. Vui lòng nhập một số từ 1 đến 7.")